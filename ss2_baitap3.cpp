#include <stdio.h>

int main() {
    int num1 = 10;
    int num2 = 20;
    int cong = num1 + num2;
    printf("%d + %d = %d\n",num1 ,num2 , cong);
    int tru = num1 - num2;
    printf("%d - %d = %d\n",num1 ,num2 , tru);
    int nhan = num1 * num2;
    printf("%d x %d = %d\n",num1 ,num2 , nhan);
    int chia = num1 / num2;
    printf("%d : %d = %d",num1 ,num2 , chia);
    return 0;
}
